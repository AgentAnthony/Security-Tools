#!/bin/bash
for c in $(cat targets); do
 for i in $(cat DES_Ciphers.txt); do 
  echo "----------------------------------------------TLSv1--------------------------------------------------------"
  echo "-----------------------------------------------------------------------------------------------------------"
  openssl s_client -connect $c:443 -tls1 -cipher $i
  echo "-----------------------------------------------------------------------------------------------------------"
  echo "----------------------------------------------TLSv1.1------------------------------------------------------"
  openssl s_client -connect $c:443 -tls1_1 -cipher $i
  echo "-----------------------------------------------------------------------------------------------------------"
  echo "---------------------------------------------TLSv1.2-------------------------------------------------------"
  openssl s_client -connect $c:443 -tls1_2 -cipher $i
  echo "-----------------------------------------------------------------------------------------------------------"
 done
done